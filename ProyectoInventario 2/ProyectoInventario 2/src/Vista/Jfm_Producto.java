    private void jbtm_guardar_productoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtm_guardar_productoActionPerformed
        //        capturar datos
        String nombre,marca,modelo;
        int idProducto,stock;
        boolean flag = true;
        Bodega bodega = new Bodega();
        
        nombre = this.jtxt_nombre_producto.getText();
        marca = this.jtxt_marca.getText();
        modelo = this.jtxt_modelo.getText();
        //Validaciones previo a ejecuci√≥n de query
        if(!(Validacion.validarNotNullString(nombre) && Validacion.validarVarchar20(nombre))){
           JOptionPane.showMessageDialog(this, "El nombre no puede tener m√°s de 20 caracteres ni estar en blanco.");
           flag = false;
        }
        else if(!(Validacion.validarNotNullString(marca) && Validacion.validarVarchar20(marca))){
           JOptionPane.showMessageDialog(this, "La marca no puede tener m√°s de 20 caracteres ni estar en blanco.");
           flag = false;
        }
        else if(!(Validacion.validarNotNullString(modelo) && Validacion.validarVarchar20(modelo))){
           JOptionPane.showMessageDialog(this, "El modelo no puede tener m√°s de 20 caracteres ni estar en blanco.");
           flag = false;
        }
        
        try{
            idProducto = Integer.valueOf(this.jtxt_id_producto.getText());
        }
        catch(Exception ex){
           JOptionPane.showMessageDialog(this, "Ingrese solo caracteres num√©ricos en el id del producto");
           flag = false;
           idProducto = 0;
        }
        if(!(Validacion.validarInt(idProducto))){
           JOptionPane.showMessageDialog(this, "El idProducto no puede tener m√°s de 9 d√≠gitos, ni estar en blanco.");
           flag = false;
        }
        
        try{
            stock = Integer.valueOf(this.jtxt_stock.getText());
            if(!(Validacion.validarInt(stock))){
                JOptionPane.showMessageDialog(this, "El stock no puede tener m√°s de 9 d√≠gitos, ni estar en blanco.");
                flag = false;
            }
        }
        catch(Exception ex){
           JOptionPane.showMessageDialog(this, "Ingrese solo caracteres num√©ricos en el stock del producto");
           flag = false;
           stock = 0;
        }

        //esta l√≠nea captura la bodega seleccionada
        bodega = (Bodega)this.jcb_bodega.getSelectedItem();        

        //EJECUCI√ìN DE QUERY
        Registro reg = new Registro();
        if(flag){
            if(reg.validarRegistroProducto(idProducto)==false){
                Producto producto = new Producto(nombre, marca, modelo, idProducto, stock, bodega);
                reg.agregarProducto(producto);
                JOptionPane.showMessageDialog(this, "Registro exitoso de " + nombre);
            }
            else {
                JOptionPane.showMessageDialog(this, "Este id de producto ya se encuentra registrado, elige otro");
            }
        }
    }//GEN-LAST:event_jbtm_guardar_productoActionPerformed

