    private void jbtn_registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_registrarActionPerformed
        //        capturar datos
        String nombre,apellido,correo,nomUsuario,contrasena;
        int celular;
        boolean flag = true;

        nombre = this.jtxt_nombre.getText();
        apellido = this.jtxt_apellido.getText();
        correo = this.jtxt_correo.getText();
        nomUsuario = this.jtxt_nombre_usuario.getText();
        contrasena = this.jtxt_registro_contrasena.getText();

        Registro reg = new Registro();
        //Validaciones antes de ejecutar query
        if(!(Validacion.validarNotNullString(nombre) && Validacion.validarVarchar20(nombre))){
            JOptionPane.showMessageDialog(this, "El nombre no puede tener m√°s de 20 caracteres ni estar en blanco.");
            flag = false;
        }
        else if(!(Validacion.validarNotNullString(apellido) && Validacion.validarVarchar20(apellido))){
            JOptionPane.showMessageDialog(this, "El apellido no puede tener m√°s de 20 caracteres ni estar en blanco.");
            flag = false;
        }
        else if(!(Validacion.validarNotNullString(correo) && Validacion.validarVarchar30(correo))){
            JOptionPane.showMessageDialog(this, "El correo no puede tener m√°s de 30 caracteres ni estar en blanco.");
            flag = false;
        }
        else if(!(Validacion.validarNotNullString(nomUsuario) && Validacion.validarVarchar20(nomUsuario))){
            JOptionPane.showMessageDialog(this, "El nombre de usuario no puede tener m√°s de 20 caracteres ni estar en blanco.");
            flag = false;
        }
        else if(!(Validacion.validarNotNullString(contrasena) && Validacion.validarVarchar20(contrasena))){
            JOptionPane.showMessageDialog(this, "La contrase√±a de usuario no puede tener m√°s de 20 caracteres ni estar en blanco.");
            flag = false;
        }

        try{
            celular = Integer.parseInt(this.jtxt_celular.getText());
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(this, "Ingrese solo caracteres num√©ricos en el celular");
            flag = false;
            celular = 0;
        }
        if(!(Validacion.validarInt(celular))){
            JOptionPane.showMessageDialog(this, "El celular no puede tener m√°s de 9 digitos ni estar en blanco.");
            flag = false;
        }
        //ejecuci√≥n query
        if(flag){
            if(reg.validarRegistroUsuario(nomUsuario)==false){
                Usuario usuario = new Usuario(nombre, apellido, correo, nomUsuario, contrasena, celular);
                reg.agregarUsuario(usuario);
                JOptionPane.showMessageDialog(this, "Registro exitoso");
            }
            else {
                JOptionPane.showMessageDialog(this, "Este nombre de usuario ya se encuentra registrado, elige otro");
            }
        }

    }//GEN-LAST:event_jbtn_registrarActionPerformed

